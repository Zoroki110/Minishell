#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	nb_char;

	nb_char = n + 48;
	if (n == -2147483648)
	{
		write (fd, "-", 1);
		write (fd, "2", 1);
		ft_putnbr_fd(147483648, fd);
	}
	else if (n < 0)
	{
		write (fd, "-", 1);
		n = -n;
		ft_putnbr_fd(n, fd);
	}
	else if (n > 9)
	{
		ft_putnbr_fd((n / 10), fd);
		ft_putnbr_fd((n % 10), fd);
	}
	else
		write (fd, &nb_char, 1);
}
